public class main {
	public static void main(string[] args) {
		int i = 0;

		//Infix operators
		true || false;
		true && false;
		1 | 2;
		1 ^ 2;
		1 & 2;
		1 == 2;
		1 != 2;
		1 < 2;
		1 > 2;
		1 <= 2;
		1 >= 2;
		1 << 2;
		1 >> 2;
		1 >>> 2;
		1 + 2;
		1 - 2;
		1 * 2;
		1 / 2;
		1 % 2;

		//Prefix operators
		++ i;
		-- i;
		! true;
		~ 1;
		+ 1;
		- 1;

		//Postfix operators
		i ++;
		i --;
	}
}
