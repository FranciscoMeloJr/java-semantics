public class main {
	static void main(String[] args) {
		int a = 0, i = 1;

		//Infix operators
		true || false;
		true && false;
		1 | 2;
		1 ^ 2;
		1 & 2;
		1 == 2;
		1 != 2;
		1 < 2;
		1 > 2;
		1 <= 2;
		1 >= 2;
		1 << 2;
		1 >> 2;
		1 >>> 2;
		1 + 2;
		1 - 2;
		1 * 2;
		1 / 2;
		1 % 2;

		//Prefix operators
		++ i;
		-- i;
		! true;
		~ 1;
		+ 1;
		- 1;

		//Postfix operators
		i ++;
		i --;

    //Ternary operator
    4 > 2 ? 4 : 2;

		//Assignment operators
		a = i;
		a += i;
		a -= i;
		a *= i;
		a /= i;
		a &= i;
		a |= i;
		a ^= i;
		a %= i;
		a <<= i;
		a >>= i;
		a >>>= i;

    System.out.println("Done!");
	}
}
